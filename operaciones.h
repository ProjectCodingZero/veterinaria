int suma (int numa, int numb)
{
	int sum;
	sum = numa+numb;
	return sum;
}

int resta (int numa, int numb)
{
	int res;
	res = numa-numb;
	return res;
}

int multiplicacion (int numa, int numb)
{
	int mult;
	mult = numa*numb;
	return mult;
}

float division (int numa, int numb)
{
	float divi;
	divi = numa/numb;
	return divi;
}
